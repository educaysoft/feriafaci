<p style="color: black; font: 1.5vw 'trebuchet MS', 'lucida sans'; font-weight: bold;">Objetivo de la  Facultad de Ciencias Admnistrativas y Económicas</p>
<p style="text-align:justify; font: 1.5vw 'trebuchet MS', 'lucida sans';   margin:10px 0 10px 0;">
Formar a los estudiantes en áreas de gestión empresarial como finanzas, contabilidad, administración de empresas, comercio para que puedan tomar decisiones estratégicas en el sector empresarial y contribuir al desarrollo sostenible de las empresas y la economía del país.</p>

<p style="color: black; font-size:1.5vw; font-weight: bold;">Objetivos del congreso:</p>
<p style="text-align:justify; font: 1.5vw 'trebuchet MS', 'lucida sans'; margin:10px 0 10px 0;">Analizar y debatir los desafíos económicos locales y globales y proponer soluciones y estrategias para afrontarlos.</p>
<p style="text-align:justify;  font: 1.5vw 'trebuchet MS', 'lucida sans';  margin:10px 0 10px 0;">Fomentar una visión crítica y reflexiva sobre la economía y su impacto en la sociedad y el medio ambiente a nivel global.</p>
<p style="text-align:justify;  font: 1.5vw 'trebuchet MS', 'lucida sans';  margin:10px 0 10px 0;">Contribuir al fortalecimiento de las relaciones y colaboración entre instituciones académicas y empresariales del área económica a nivel nacional e internacional.</p>

<p style="color: black; font: 1.5vw 'trebuchet MS', 'lucida sans';  font-weight: bold;">¿A quien va dirigido?</p>
<p style="text-align:justify;  font: 1.5vw 'trebuchet MS', 'lucida sans';  margin:10px 0 10px 0;">
Profesores, investigadores, consultores y estudiantes de grado o posgrado que hayan realizado o estén desarrollando trabajos académicos con casos nacionales o con una perspectiva comparada, ya sea con una mirada inter/transdisciplinar o bien disciplinar desde la sociología, ciencias políticas, economía, antropología, historia, ciencias agropecuarias, etc.</p>
